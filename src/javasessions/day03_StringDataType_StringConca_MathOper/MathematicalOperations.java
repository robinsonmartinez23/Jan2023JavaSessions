package javasessions.day03_StringDataType_StringConca_MathOper;

public class MathematicalOperations {
    public static void main(String[] args) {
        System.out.println(4/2);
        System.out.println(4+2);
        System.out.println(4-2);
        System.out.println(4*2);

        System.out.println("-----------");

        System.out.println(9/2); //     int/int = int = 4
        System.out.println(9.0/2); //   float/int = float = 4.5
        System.out.println(9/2.0); //   int/float = float = 4.5
        System.out.println(9.0/2.0); // float/float = float = 4.5

    }
}
